<div class="row">
    <div class="col-md-12">
        <p>
            <div class="alert alert-info">
                En PHP, le nom d'une variable commence toujours par un "$". <br>
                Le $ est suivi par un underscore ou une lettre. Ex : $_ma_variable, $uneVariable. <br>
                Un nom de variable ne peut pas contenir de caractères spéciaux. <br>
                Il est important de choisir des noms de variables explicites pour faciliter la lecture et la compréhension de votre code.
                En PHP, les noms de variables sont sensibles à la casse, c'est-à-dire que $maVariable et $mavariable sont considérées comme deux variables différentes.
            </div>
        </p>
    </div>
</div>
